bool Line_Shape::Draw(D3DXVECTOR3 vStart, D3DXVECTOR3 vEnd, D3DXVECTOR4 vColor)
{
	PNCT_VERTEX vertexList[2];
	vertexList[0] = m_VertexList[0];
	vertexList[0].p = vStart;
	vertexList[0].c = vColor;

	vertexList[1] = m_VertexList[1];
	vertexList[1].p = vEnd;
	vertexList[1].c = vColor;

	g_pD3dContext->UpdateSubresource(m_dxObj.m_pVertexBuffer.Get(), 0, nullptr, &vertexList, 0, 0);

	m_Primitive = D3D_PRIMITIVE_TOPOLOGY_LINELIST;
	Shape_DX::Render();

	m_Primitive = D3D_PRIMITIVE_TOPOLOGY_TRIANGLELIST;
	g_pD3dContext->IASetPrimitiveTopology(m_Primitive);

	

	return true;
}

